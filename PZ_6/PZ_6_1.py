for i in range(N): #При заданной длине, на каждой итерации добавляем новый элемент в список
    a.append(int(input('Введите число для добавдения в список a: ')))

for i in range(K, L + 1):  # Находим сумму элементов списка от K до L, включительно
        sum += a[i]

print(f'Сумма равна {sum}')

