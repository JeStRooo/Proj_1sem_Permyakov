#Дан список размера N и целые числа от K и L (1<K<L<N). Найти сумму элементов в данном списке.
N = input('Введите размер списка: ')
K = input('Введите K: ')
L = input('Введите L: ')
a = [] #Наш пустой список
sum = 0

while (type(N) != int) & (type(K) != int) & (type(L) != int): #Обработка исключений
    try:
        N = int(N)
        K = int(K)
        L = int(L)

    except ValueError:
        print('Вы неправильно ввели значение!')
        N = input('Введите размер списка: ')
